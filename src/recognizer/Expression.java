package recognizer;

public class Expression {
private String name;
private String left;
private String right;
private String op;
private String rangLeft;
private String rangRight;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getLeft() {
	return left;
}
public void setLeft(String left) {
	this.left = left;
}
public String getRight() {
	return right;
}
public void setRight(String right) {
	this.right = right;
}
public String getOp() {
	return op;
}
public void setOp(String op) {
	this.op = op;
}
public String getRangLeft() {
	return rangLeft;
}
public void setRangLeft(String rangLeft) {
	this.rangLeft = rangLeft;
}
public String getRangRight() {
	return rangRight;
}
public void setRangRight(String rangRight) {
	this.rangRight = rangRight;
}

}
