package recognizer;

public class Etudiant {
	
	public String getNum_Insc_Etud() {
		return num_Insc_Etud;
	}
	public void setNum_Insc_Etud(String numInscEtud) {
		num_Insc_Etud = numInscEtud;
	}
	public String getNom_Etud() {
		return nom_Etud;
	}
	public void setNom_Etud(String nomEtud) {
		nom_Etud = nomEtud;
	}
	public String getPrenom_Etud() {
		return prenom_Etud;
	}
	public void setPrenom_Etud(String prenomEtud) {
		prenom_Etud = prenomEtud;
	}
	public String getGroupe_Etud() {
		return groupe_Etud;
	}
	public void setGroupe_Etud(String groupeEtud) {
		groupe_Etud = groupeEtud;
	}
	private String num_Insc_Etud;
	private String nom_Etud;
	private String prenom_Etud;
	private String groupe_Etud;
	
}
